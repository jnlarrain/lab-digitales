`timescale 1ns / 1ps

module led_mgmt(
    input [7:0] data,
    input en,
    output [15:0] led
    );
    
    assign led = (!en) ? 16'b0000000000000000
               : (data < 16) ? 16'b0000000000000001
               : (data < 32) ? 16'b0000000000000010
               : (data < 48) ? 16'b0000000000000100
               : (data < 64) ? 16'b0000000000001000
               : (data < 80) ? 16'b0000000000010000
               : (data < 96) ? 16'b0000000000100000
               : (data < 112) ? 16'b0000000001000000
               : (data < 128) ? 16'b0000000010000000
               : (data < 144) ? 16'b0000000100000000
               : (data < 160) ? 16'b0000001000000000
               : (data < 176) ? 16'b0000010000000000
               : (data < 192) ? 16'b0000100000000000
               : (data < 208) ? 16'b0001000000000000
               : (data < 224) ? 16'b0010000000000000
               : (data < 240) ? 16'b0100000000000000
               : 16'b1000000000000000;
               
endmodule